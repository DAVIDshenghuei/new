#include "libft.h"

int	ft_isalpha(int _c)
{
	if (_c >= 'a' && _c <= 'z')
		return (FT_TRUE);
	if (_c >= 'A' && _c <= 'Z')
		return (FT_TRUE);
	return (FT_FALSE);
}
